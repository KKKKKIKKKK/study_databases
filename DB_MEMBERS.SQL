
CREATE TABLE  MEMBERS  
(
  Name            VARCHAR(50) NOT NULL COMMENT '이름',
  Age             INTEGER     NOT NULL COMMENT '나이',
  Phone Number    VARCHAR(50) NOT NULL COMMENT '전화번호',
  MEMBERS_ID      VARCHAR(50) NOT NULL COMMENT 'UNIQUE_ID',
  GENDER_CODE     VARCHAR(50) NOT NULL,
  Address_CODE_ID VARCHAR(50) NOT NULL COMMENT '주소 ',
  PRIMARY KEY (MEMBERS_ID)
) COMMENT '회원정보';

CREATE TABLE  MEMBERS_ HOBBY 
(
  HOBBY_CODE_ID VARCHAR(50) NOT NULL,
  MEMBERS_ID    VARCHAR(50) NOT NULL COMMENT 'UNIQUE_ID'
) COMMENT '회원_취미';

CREATE TABLE  MEMBERS_Login
(
  E-MAIL            VARCHAR(50) NOT NULL COMMENT '이메일',
   MEMBERS_LOGIN_ID             NOT NULL COMMENT '회원 로그인',
   MEMBERS_ID       VARCHAR(50) NOT NULL COMMENT '유니크ID',
  PRIMARY KEY ( MEMBERS_LOGIN_ID)
) COMMENT '회원정보 로그인';

CREATE TABLE ADDRESS_CODE
(
  ADDRESS         VARCHAR     NOT NULL COMMENT '주소',
  ADDRESS_CODE_ID VARCHAR(50) NOT NULL,
  PRIMARY KEY (ADDRESS_CODE_ID)
) COMMENT '주소코드';

CREATE TABLE GENDER_CODE
(
  GENDER      VARCHAR     NOT NULL COMMENT '성별',
  GENDER_CODE VARCHAR(50) NOT NULL,
  PRIMARY KEY (GENDER_CODE)
) COMMENT '성별코드';

CREATE TABLE HOBBY_CODE 
(
  HOBBY         VARCHAR(50) NOT NULL COMMENT '취미명',
  HOBBY_CODE_ID VARCHAR(50) NOT NULL,
  PRIMARY KEY (HOBBY_CODE_ID)
) COMMENT '취미코드';

ALTER TABLE  MEMBERS_Login
  ADD CONSTRAINT FK_ MEMBERS  _TO_ MEMBERS_Login
    FOREIGN KEY ( MEMBERS_ID)
    REFERENCES  MEMBERS   (MEMBERS_ID);

ALTER TABLE  MEMBERS  
  ADD CONSTRAINT FK_GENDER_CODE_TO_ MEMBERS  
    FOREIGN KEY (GENDER_CODE)
    REFERENCES GENDER_CODE (GENDER_CODE);

ALTER TABLE  MEMBERS_ HOBBY 
  ADD CONSTRAINT FK_HOBBY_CODE _TO_ MEMBERS_ HOBBY 
    FOREIGN KEY (HOBBY_CODE_ID)
    REFERENCES HOBBY_CODE  (HOBBY_CODE_ID);

ALTER TABLE  MEMBERS_ HOBBY 
  ADD CONSTRAINT FK_ MEMBERS  _TO_ MEMBERS_ HOBBY 
    FOREIGN KEY (MEMBERS_ID)
    REFERENCES  MEMBERS   (MEMBERS_ID);

ALTER TABLE  MEMBERS  
  ADD CONSTRAINT FK_ADDRESS_CODE_TO_ MEMBERS  
    FOREIGN KEY (Address_CODE_ID)
    REFERENCES ADDRESS_CODE (ADDRESS_CODE_ID);
